package com.hollysys.hiadsp.common.model.org;

public class OrgAttachUser {
    private String code;
    private String name;
    private String managerCode;
    private String managerName;
    private String managerJobNumber;
    private String managerPost;
    private String parentCode;
    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getManagerCode() {
        return managerCode;
    }
    public void setManagerCode(String managerCode) {
        this.managerCode = managerCode;
    }
    public String getManagerName() {
        return managerName;
    }
    public void setManagerName(String managerName) {
        this.managerName = managerName;
    }
    public String getManagerJobNumber() {
        return managerJobNumber;
    }
    public void setManagerJobNumber(String managerJobNumber) {
        this.managerJobNumber = managerJobNumber;
    }
    public String getParentCode() {
        return parentCode;
    }
    public void setParentCode(String parentCode) {
        this.parentCode = parentCode;
    }
    public String getManagerPost() {
        return managerPost;
    }
    public void setManagerPost(String managerPost) {
        this.managerPost = managerPost;
    }
    
}
